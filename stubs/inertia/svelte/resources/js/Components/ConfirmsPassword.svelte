<script>
    import { createEventDispatcher } from 'svelte'
    import JetButton from './Button.svelte'
    import JetDialogModal from './DialogModal.svelte'
    import JetInput from './Input.svelte'
    import JetInputError from './InputError.svelte'
    import JetSecondaryButton from './SecondaryButton.svelte'

    const dispatch = createEventDispatcher()

    export let title = "Confirm Password"
    export let content = "For your security, please confirm your password to continue."
    export let button = "Confirm"
    
    let confirmingPassword = false
    let passwordInput

    let password = ''
    let error = ''
    let processing = false


    const startConfirmingPassword = () => {
        axios.get(route('password.confirmation')).then(response => {
            if (response.data.confirmed) {
                dispatch('confirmed');
            } else {
                confirmingPassword = true;

                setTimeout(() => passwordInput.focus(), 250);
            }
        })
    }

    const confirmPassword = () => {
        processing = true

        axios.post(route('password.confirm'), {
            password: password,
        }).then(() => {
            processing = false

            closeModal()
            dispatch('confirmed')

        }).catch(error => {
            processing = false
            error = error.response.data.errors.password[0]
            passwordInput.focus()
        });
    }

    const closeModal = () => {
        confirmingPassword = false
        password = ''
        error = ''
    }
</script>

<span>
    <span on:click={startConfirmingPassword}>
        <slot />
    </span>

    <JetDialogModal show={confirmingPassword} on:close={closeModal}>
        <svelte:fragment slot="title">
            { title }
        </svelte:fragment>

        <svelte:fragment slot="content">
            { content }

            <div class="mt-4">
                <JetInput
                    bind:this={passwordInput}
                    bind:value={password}
                    type="password"
                    class="mt-1 block w-3/4"
                    placeholder="Password"
                    on:keyup={(e) => e.key === 'Enter' && confirmPassword()}/>

                <JetInputError message={error} class="mt-2" />
            </div>
        </svelte:fragment>

        <svelte:fragment slot="footer">
            <JetSecondaryButton on:click={closeModal}>
                Cancel
            </JetSecondaryButton>

            <JetButton
                class="ml-3 { processing ? 'opacity-25' : ''}"
                disabled={processing}
                on:click={confirmPassword}>
                { button }
            </JetButton>
        </svelte:fragment>
    </JetDialogModal>
</span>
